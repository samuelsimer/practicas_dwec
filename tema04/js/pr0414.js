//CREAR PRODUCTO
function renderProduct(id, name, price, image){
    let product = document.createElement('div');
    product.classList.add('product');
    let imagen = document.createElement('img');
    imagen.src = image;
    imagen.classList.add('product-photo');
    product.append(imagen);
    let dataName = document.createElement('div');
    dataName.classList.add('product-dataName');
    product.append(dataName);
    let nombre = document.createElement('h3');
    nombre.textContent = name;
    nombre.classList.add('fruit-name');
    dataName.append(nombre);
    let data = document.createElement('div');
    data.classList.add('product-data');
    product.append(data);
    let precio = document.createElement('h3');
    precio.textContent = price;
    precio.classList.add('price');
    data.append(precio);
    let boton = document.createElement('button');
    boton.textContent = 'Añadir';
    boton.classList.add('btn-add-product');
    boton.setAttribute('data-id', id);
    //acceder con boton.dataset.id
    data.append(boton);
    document.getElementById('products-section').append(product);
}

